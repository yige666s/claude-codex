package agentruntime

import (
	"context"
	"strings"
	"testing"
)

func TestPromptOptimizationCreatesReviewCandidateWhenReplayRegresses(t *testing.T) {
	ctx := context.Background()
	store := NewMemoryPromptStore()
	if _, err := store.UpsertPrompt(ctx, PromptTemplate{ID: "rag_answer", Name: "RAG Answer"}); err != nil {
		t.Fatalf("upsert prompt: %v", err)
	}
	if _, err := store.CreatePromptVersion(ctx, PromptVersion{PromptID: "rag_answer", Version: "v1", Status: PromptStatusPublished, Content: "Answer with evidence."}); err != nil {
		t.Fatalf("create baseline: %v", err)
	}
	server := NewServer(NewRuntime(RuntimeConfig{}, nil, nil, nil, nil), HeaderAuthenticator{UserHeader: "X-User-ID"}, NoopRateLimiter{}, nil)
	server.SetPromptStore(store)

	out, err := server.promptOptimizationCreateReview(ctx, nil, map[string]any{
		"prompt_id":           "rag_answer",
		"baseline_version":    "v1",
		"candidate_version":   "opt-regressed",
		"candidate_content":   "Answer with evidence.\n\nPrompt optimization review notes.",
		"candidate_changelog": "Generated by prompt optimization.",
		"actor":               "admin",
		"offline_replay":      "completed",
		"baseline_pass_rate":  1.0,
		"candidate_pass_rate": 0.2,
	})
	if err != nil {
		t.Fatalf("create review: %v", err)
	}
	if out["review_status"] != PromptStatusReviewPending || out["review_recommendation"] != "blocked_by_eval_regression" {
		t.Fatalf("unexpected review output: %#v", out)
	}
	version, err := store.GetPromptVersion(ctx, "rag_answer", "opt-regressed")
	if err != nil {
		t.Fatalf("candidate version not created: %v", err)
	}
	if version.Status != PromptStatusReviewPending || !strings.Contains(version.Changelog, "candidate pass rate is below baseline") {
		t.Fatalf("regressed candidate should remain in review with warning changelog: %#v", version)
	}
}
