import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const repoRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const configPath = path.join(repoRoot, "scripts/live_transcript_noise.json");
const goPath = path.join(repoRoot, "internal/backend/agentruntime/live_noise_config_gen.go");
const tsPath = path.join(repoRoot, "apps/web/src/features/workspace/liveTranscriptNoiseConfig.ts");

const config = JSON.parse(fs.readFileSync(configPath, "utf8"));

function compact(value) {
  return String(value || "").trim().toLowerCase().replace(/\s+/g, "");
}

function uniqueCompact(values) {
  return [...new Set((values || []).map(compact).filter(Boolean))].sort();
}

const generated = {
  version: Number(config.version || 1),
  minMeaningfulRunes: Number(config.minMeaningfulRunes || 2),
  repeatedSingleRuneMinRunes: Number(config.repeatedSingleRuneMinRunes || 4),
  meaningfulShortUtterances: uniqueCompact(config.meaningfulShortUtterances),
  standaloneFillers: uniqueCompact(config.standaloneFillers),
  repeatableFillers: uniqueCompact(config.repeatableFillers),
  shortContains: (config.shortContains || [])
    .map((item) => ({ value: compact(item.value), maxRunes: Number(item.maxRunes || 0) }))
    .filter((item) => item.value && item.maxRunes > 0)
    .sort((a, b) => a.value.localeCompare(b.value))
};

const header = `// Code generated by scripts/generate-live-transcript-noise-config.mjs from scripts/live_transcript_noise.json; DO NOT EDIT.\n`;

function goStringArray(values) {
  if (!values.length) return "nil";
  return "[]string{\n" + values.map((value) => `\t\t${JSON.stringify(value)},`).join("\n") + "\n\t}";
}

function goShortContains(values) {
  if (!values.length) return "nil";
  return "[]liveTranscriptNoiseShortContains{\n" + values.map((item) => `\t\t{Value: ${JSON.stringify(item.value)}, MaxRunes: ${item.maxRunes}},`).join("\n") + "\n\t}";
}

fs.writeFileSync(goPath, `${header}
package agentruntime

type liveTranscriptNoiseShortContains struct {
\tValue    string
\tMaxRunes int
}

type liveTranscriptNoiseRules struct {
\tVersion                    int
\tMinMeaningfulRunes         int
\tRepeatedSingleRuneMinRunes int
\tMeaningfulShortUtterances   []string
\tStandaloneFillers          []string
\tRepeatableFillers          []string
\tShortContains              []liveTranscriptNoiseShortContains
}

var liveTranscriptNoise = liveTranscriptNoiseRules{
\tVersion:                    ${generated.version},
\tMinMeaningfulRunes:         ${generated.minMeaningfulRunes},
\tRepeatedSingleRuneMinRunes: ${generated.repeatedSingleRuneMinRunes},
\tMeaningfulShortUtterances:   ${goStringArray(generated.meaningfulShortUtterances)},
\tStandaloneFillers:          ${goStringArray(generated.standaloneFillers)},
\tRepeatableFillers:          ${goStringArray(generated.repeatableFillers)},
\tShortContains:              ${goShortContains(generated.shortContains)},
}
`);

fs.writeFileSync(tsPath, `${header}
export type LiveTranscriptNoiseShortContains = {
  value: string;
  maxRunes: number;
};

export type LiveTranscriptNoiseRules = {
  version: number;
  minMeaningfulRunes: number;
  repeatedSingleRuneMinRunes: number;
  meaningfulShortUtterances: readonly string[];
  standaloneFillers: readonly string[];
  repeatableFillers: readonly string[];
  shortContains: readonly LiveTranscriptNoiseShortContains[];
};

export const liveTranscriptNoise: LiveTranscriptNoiseRules = ${JSON.stringify(generated, null, 2)} as const;
`);
